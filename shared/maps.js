export const MAP_IDS = Object.keys(MAPS);
export const DEFAULT_MAP_ID = 'default';
export const DEFAULT_MAP = MAPS[DEFAULT_MAP_ID];

export function isValidMapId(id) {
  return typeof id === 'string' && id in MAPS;
}

export function normalizeMapId(id) {
  return isValidMapId(id) ? id : DEFAULT_MAP_ID;
}

export function getMap(id) {
  return MAPS[normalizeMapId(id)];
}

export function parseMap(mapString = DEFAULT_MAP) {
  const rows = mapString.split('\n');
  const height = rows.length;
  const width = rows.reduce((m, r) => Math.max(m, r.length), 0);

  const walls = [];
  const solidWalls = [];
  const bases = { team1: null, team2: null };
  let flag = null;

  for (let y = 0; y < height; y++) {
    const row = rows[y];
    let wallRunStart = -1;
    let solidRunStart = -1;
    for (let x = 0; x <= width; x++) {
      const ch = row[x];

      if (ch === TILES.WALL) {
        if (wallRunStart === -1) wallRunStart = x;
      } else if (wallRunStart !== -1) {
        walls.push(makeWall(wallRunStart, x - 1, y));
        wallRunStart = -1;
      }

      if (ch === TILES.SOLID) {
        if (solidRunStart === -1) solidRunStart = x;
      } else if (solidRunStart !== -1) {
        solidWalls.push(makeSolid(solidRunStart, x - 1, y));
        solidRunStart = -1;
      }

      if (ch === TILES.TEAM1_BASE) bases.team1 = tileCenter(x, y);
      else if (ch === TILES.TEAM2_BASE) bases.team2 = tileCenter(x, y);
      else if (ch === TILES.FLAG) flag = tileCenter(x, y);
    }
  }

  return {
    rows,
    width,
    height,
    pixelWidth: width * TILE.WIDTH,
    pixelHeight: height * TILE.HEIGHT,
    walls,
    solidWalls,
    bases,
    flag,
  };
}

function tileCenter(tx, ty) {
  return {
    x: tx * TILE.WIDTH + TILE.WIDTH / 2,
    y: ty * TILE.HEIGHT + TILE.HEIGHT / 2,
  };
}

function makeWall(startX, endX, ty) {
  const tiles = endX - startX + 1;
  const w = tiles * TILE.WIDTH;
  const h = TILE.HEIGHT;
  return {
    x: startX * TILE.WIDTH + w / 2,
    y: ty * TILE.HEIGHT + h / 2,
    w,
    h,
  };
}

function makeSolid(startX, endX, ty) {
  const tiles = endX - startX + 1;
  const w = tiles * TILE.WIDTH;
  const h = TILE.HEIGHT;
  return {
    x: startX * TILE.WIDTH + w / 2,
    y: ty * TILE.HEIGHT + h / 2,
    w,
    h,
  };
}
